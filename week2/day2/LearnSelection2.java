package week1.day2;

public class LearnSelection2 {

	public static void main(String[] args) {
		int num = 0;
	
		if(num >0) {
			System.out.println("Positive number: "+ num);
		}else if (num ==0){
			System.out.println("Neutral number: "+ num);
		}
		else {
			System.out.println("Negative number: "+ num);
		}
	}
}
